# Python Tutorial: Band Name Generator

# Welcome message to the user
print("Welcome to the Band Name Generator.")

# Prompt the user to input the name of the city they grew up in
city_name = input("What's the name of the city you grew up in?\n")

# Prompt the user to input their pet's name
pet_name = input("What's your pet's name?\n")

# Generate and display the potential band name by combining the city name and pet name
print("Your band name could be " + city_name + " " + pet_name)
