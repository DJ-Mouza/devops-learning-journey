# Python If and Else Statement Tutorial

# The basic structure of an if-else statement:
# if condition:
#     do this
# else:
#     do this

# Practical Example: Water Level Check

# Set the water level value
water_level = 50

# Check if the water level is greater than 80
if water_level > 80:
    print("Drain water")  # If the water level is greater than 80, print this message

else:
    print("Continue")  # If the water level is not greater than 80, print this message

#                Comparator Operators
# These operators are used to compare values in conditional statements:

# >  : Greater than
# <  : Less than
# >= : Greater than or equal to
# <= : Less than or equal to
# == : Equal to
# != : Not equal to

# *Important note: 
# '=' is used for assigning a value to a variable
# '==' is used to check equality between two values